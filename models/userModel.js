import mongoose from "mongoose";

const userSchema = new mongoose.Schema({
    name:{
        type:String,
        required:true
    },
    email:{
        type:String,
        unique:true,
        required:true
    },
    password:{
        type:String,
        required:true
    },
    role:{
        type:String,
        enum:["Candidate","Recruiter","Admin"],
        required:true
    }
},{timestamps:true})

export const userModel = mongoose.model("users",userSchema)